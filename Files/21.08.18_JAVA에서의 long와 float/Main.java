public class Main{
    public static void main(String[] args){
        long l = 700000000L; //long의 큰 수를 사용할땐 값 끝에 L을 붙어야한다
        //long l = 700; (O)
        float f = 9.8F; //float를 사용할땐 값 끝에 F를 붙어야한다
        boolean b = true;
        
        System.out.println(l);
        System.out.println(f);
        System.out.println(b);
    }
}
